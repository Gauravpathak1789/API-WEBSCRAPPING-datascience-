# Q2. What are the different methods used for Web Scraping?

# In[2]:


#Manual scraping: This involves manually copying and pasting data from websites into a spreadsheet or a database. It is a time-consuming process and is usually only feasible for small amounts of data.

#Parsing HTML: This involves parsing the HTML code of a website to extract the relevant data. This can be done using programming languages such as Python, Ruby, or JavaScript. Libraries such as BeautifulSoup, lxml, and HTMLParser can be used to parse the HTML.

#Web scraping tools: There are many web scraping tools available that can automate the scraping process. These tools typically have a user-friendly interface that allows users to select the data they want to extract and specify the websites they want to scrape.


