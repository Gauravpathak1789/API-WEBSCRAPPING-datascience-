# Q1. Explain GET and POST methods

# In[1]:


#The GET method is used to retrieve data from a server. When a GET request is made, the server returns the requested resource, which is usually a webpage or data in some other format such as JSON or XML.
#The POST method is used to submit data to a server. When a POST request is made, the data is sent in the body of the request, rather than being appended to the URL like in a GET request.


